    bool isAdded = false;
    for (int i = 0; i < message.length; i++) {
      isAdded = false;
      for (int x = 0; x < abcLat.length; x++) {
        if (message[i] == abcLat[x]) {
          builder = builder + abcCyr[x];
          isAdded = true;
          break;
        }
      }
      if (!isAdded) builder = builder + message[i];
    }
    return builder.toString();
  }
